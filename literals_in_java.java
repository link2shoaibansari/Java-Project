public class literals_in_java {
    public static void main(String[] args) {
        int age1 = 50;
        byte age2 = 55;
        short age3 = 10;
        float f1 = 5000f;
        long l1 = 500000000L;
        System.out.println(age1+" and "+age2);
        System.out.println(f1+" and "+l1);
        System.out.println(age3);
    }
}
